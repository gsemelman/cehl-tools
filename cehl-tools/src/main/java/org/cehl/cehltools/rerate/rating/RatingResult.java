package org.cehl.cehltools.rerate.rating;

public class RatingResult {

	private double it;
	private double sp;
	private double st;
	private double en;
	private double du;
	private double di;
	private double sk;
	private double pa;
	private double pc;
	private double df;
	private double sc;
	private double ex;
	private double ld;
	private double ov;
	
	public RatingResult(double it, double sp, double st, double en, double du, double di, double sk, double pa,
			double pc, double df, double sc, double ex, double ld, double ov) {
		super();
		this.it = it;
		this.sp = sp;
		this.st = st;
		this.en = en;
		this.du = du;
		this.di = di;
		this.sk = sk;
		this.pa = pa;
		this.pc = pc;
		this.df = df;
		this.sc = sc;
		this.ex = ex;
		this.ld = ld;
		this.ov = ov;
	}
	public double getIt() {
		return it;
	}
	public void setIt(double it) {
		this.it = it;
	}
	public double getSp() {
		return sp;
	}
	public void setSp(double sp) {
		this.sp = sp;
	}
	public double getSt() {
		return st;
	}
	public void setSt(double st) {
		this.st = st;
	}
	public double getEn() {
		return en;
	}
	public void setEn(double en) {
		this.en = en;
	}
	public double getDu() {
		return du;
	}
	public void setDu(double du) {
		this.du = du;
	}
	public double getDi() {
		return di;
	}
	public void setDi(double di) {
		this.di = di;
	}
	public double getSk() {
		return sk;
	}
	public void setSk(double sk) {
		this.sk = sk;
	}
	public double getPa() {
		return pa;
	}
	public void setPa(double pa) {
		this.pa = pa;
	}
	public double getPc() {
		return pc;
	}
	public void setPc(double pc) {
		this.pc = pc;
	}
	public double getDf() {
		return df;
	}
	public void setDf(double df) {
		this.df = df;
	}
	public double getSc() {
		return sc;
	}
	public void setSc(double sc) {
		this.sc = sc;
	}
	public double getEx() {
		return ex;
	}
	public void setEx(double ex) {
		this.ex = ex;
	}
	public double getLd() {
		return ld;
	}
	public void setLd(double ld) {
		this.ld = ld;
	}
	public double getOv() {
		return ov;
	}
	public void setOv(double ov) {
		this.ov = ov;
	}
	
	
	
	
}

